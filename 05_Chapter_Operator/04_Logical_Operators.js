//Logical Operators

// Logical operators are used to combine multiple conditions and return a boolean value (true or false) based on the evaluation of those conditions.

let a = true;
let b = false;
let c = true;

console.log(a && b); // false
console.log(a && c); // true
console.log(a || b); // true
console.log(b || c); // true
console.log(!a); // false
console.log(!b); // true

// The logical AND (&&) operator returns true if both operands are true, otherwise it returns false.
// The logical OR (||) operator returns true if at least one of the operands is true, otherwise it returns false.
// The logical NOT (!) operator negates the value of the operand, returning true if the operand is false, and false if the operand is true.

