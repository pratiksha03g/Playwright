// Comparison Operators 

// Comparison operators are used to compare two values and return a boolean value (true or false) based on the comparison.  
// =, ==, ===, !=, !==, >, <, >=, <=
let x = 10;
let y = 20;
let z = 10;

console.log(x == y); // false // false because both values are not same
console.log(x == z); // true // true because both values are same
console.log(x === z); // true // true because both value and type are same
console.log(x != y); // true // true because values are not same
console.log(x !== z); // false // false because both value and type are same
console.log(x > y); // false // false because x is not greater than y
console.log(x < y); // true // true because x is less than y
console.log(x >= z); // true // true because x is greater than or equal to z
console.log(x <= z); // true // true because x is less than or equal to z

